import { model, Schema } from 'mongoose';

//ts
import { IProduct } from '../ts';

const UserSchema = new Schema<IProduct>(
  {
    name: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    stock: {
      type: Number,
      required: true,
    },
  },
  { timestamps: true }
);

export const ProductModel = model('Product', UserSchema);
